package day2;

public class Datatype {
    public static void main(String[] args) {
        int number1=12345;
        float number2=3.54f;
        double number3=9988765;
        long number4=1234567890;
        char letter='e';
        String name="easki";
        System.out.println("It is integer:" +number1);
        System.out.println("It is float:" +number2);
        System.out.println("It is double:" +number3);
        System.out.println("It is double:" +number4);
        System.out.println("It is long:" +letter);
        System.out.println("It is char:" +name);
    }
}
